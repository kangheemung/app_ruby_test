# A 君はとある宝くじを買うことにしました、その宝くじでは当選した場合、かけ金の 2 乗の金額が返金されます。
# A 君のかけ金 N 円が与えられるので、A 君が当選した場合に返金される金額を求めるプログラムを作成してください。

# 入力例 1 の場合
# 180
# A 君のかけ金が 180 円で、その 2 乗の金額が返金されるので
# 32400
# と出力してください。
# 評価ポイント
# 10回のテストケースで、正答率、実行速度、メモリ消費量をはかり得点が決まります。
# より早い回答時間で提出したほうが得点が高くなります。
# 複数のテストケースで正しい出力がされるか評価（+50点）
# 解答までの速さ評価（+50点）
# 入力される値
# 入力は以下のフォーマットで与えられます。

# N
