#あなたはカレンダーのプログラムを作成しています。

#クリスマスイヴなど記念日の前日をイヴとして計算して表示しようと考えてます。

#日付が入力されるのでその日付の 1 日前を出力してください。

#入力される値入力は以下のフォーマットで与えられます。
#`N`
#・1 行目に日付 N が与えられます。ok
#・入力は合計で 1 行となり、入力値最終行の末尾に改行が 1 つ入ります。
#それぞれの値は文字列で標準入力から渡されます。[標準入力からの値取得方法はこちらをご確認ください](https://paiza.jp/guide/samplecode.html)

#**期待する出力**日付が入力されるのでその日付の 1 日前を出力してください。
#末尾に改行を入れ、余計な文字、空行を含んではいけません。

#**条件**すべてのテストケースにおいて、以下の条件をみたします。
# ・2 ≦ N ≦ 31

#まず入力された日付を入力してください
require 'date'

# 日付の入力
# N = gets.chomp.to_i
N = 2

# 入力された日付が範囲外の場合はエラーメッセージを出力して終了
if N < 2 || N > 31
  puts "入力された日付が範囲外です"
  exit
end

# 入力された日付を文字列として作成（年月は仮で設定）
date_string = "2022-01-#{N}"

# 入力された日付をDateオブジェクトに変換
date = Date.parse(date_string)

# 1日前の日付を計算
previous_date = date - 1

# 1日前の日付を「日」のみ表示
puts previous_date.day
